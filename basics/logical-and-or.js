let temp = 65

// Logical AND Operator
if (temp >= 60 && temp <= 90) {
    console.log('It is pretty nice out')
} else if 
// Logical OR Operator
(temp <= 0 || temp >= 90) {
    console.log('Do not go outside')
} else {
    console.log('Eh. Do what you want')
}


